const fs = require('fs');
const path = require('path');

const MESSAGES_DIR = path.join(__dirname, '../src/i18n/messages/en');
const OUTPUT_FILE = path.join(__dirname, '../src/i18n/generated-types.ts');

function getAllJsonFiles(dir, baseDir = dir) {
  const files = [];
  const items = fs.readdirSync(dir, { withFileTypes: true });

  for (const item of items) {
    const fullPath = path.join(dir, item.name);
    if (item.isDirectory()) {
      files.push(...getAllJsonFiles(fullPath, baseDir));
    } else if (item.name.endsWith('.json')) {
      const relativePath = path.relative(baseDir, fullPath);
      files.push({
        fullPath,
        relativePath,
        namespace: item.name.replace('.json', ''),
        directory: path.dirname(relativePath).replace(/\\/g, '/'),
      });
    }
  }

  return files;
}

function extractKeys(obj, prefix = '') {
  const keys = [];

  for (const key of Object.keys(obj)) {
    const fullKey = prefix ? `${prefix}.${key}` : key;
    const value = obj[key];

    if (typeof value === 'object' && value !== null && !Array.isArray(value)) {
      keys.push(fullKey);
      keys.push(...extractKeys(value, fullKey));
    } else {
      keys.push(fullKey);
    }
  }

  return keys;
}

function generateTypeFromObject(obj, indent = '  ') {
  const lines = ['{'];

  for (const key of Object.keys(obj)) {
    const value = obj[key];

    if (typeof value === 'object' && value !== null && !Array.isArray(value)) {
      const nestedType = generateTypeFromObject(value, indent + '  ');
      lines.push(`${indent}  '${key}': ${nestedType};`);
    } else {
      lines.push(`${indent}  '${key}': string;`);
    }
  }

  lines.push(`${indent}}`);
  return lines.join('\n');
}

function generateTypes() {
  console.log('Scanning JSON files in:', MESSAGES_DIR);

  const files = getAllJsonFiles(MESSAGES_DIR);
  console.log(`Found ${files.length} JSON files`);

  const allMessages = {};
  const allKeys = [];

  for (const file of files) {
    console.log(`Processing: ${file.relativePath}`);
    const content = fs.readFileSync(file.fullPath, 'utf-8');
    const json = JSON.parse(content);

    allKeys.push(...extractKeys(json));

    if (file.directory && file.directory !== '.') {
      const parts = file.directory.split('/');
      let current = allMessages;

      for (const part of parts) {
        if (!current[part]) {
          current[part] = {};
        }
        current = current[part];
      }

      current[file.namespace] = json;
    } else {
      allMessages[file.namespace] = json;
    }
  }

  const uniqueKeys = [...new Set(allKeys)].sort();
  console.log(`Total unique keys: ${uniqueKeys.length}`);

  const messagesType = generateTypeFromObject(allMessages);

  const typeDefinition = `// Auto-generated by scripts/generate-i18n-types.js
// DO NOT EDIT MANUALLY - changes will be overwritten

export type IntlMessages = ${messagesType};

export type TranslationKey = 
${uniqueKeys.map(key => `  | '${key}'`).join('\n')};
`;

  const outputDir = path.dirname(OUTPUT_FILE);
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  fs.writeFileSync(OUTPUT_FILE, typeDefinition, 'utf-8');
  console.log(`\nGenerated types written to: ${OUTPUT_FILE}`);
  console.log(`Total translation keys: ${uniqueKeys.length}`);

  return {
    filesProcessed: files.length,
    keysGenerated: uniqueKeys.length,
  };
}

try {
  const result = generateTypes();
  console.log('\n✅ Type generation completed successfully!');
  console.log(`   Files processed: ${result.filesProcessed}`);
  console.log(`   Keys generated: ${result.keysGenerated}`);
} catch (error) {
  console.error('❌ Error generating types:', error);
  process.exit(1);
}
